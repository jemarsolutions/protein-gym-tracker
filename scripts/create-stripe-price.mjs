import Stripe from 'stripe';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { config } from 'dotenv';
config();

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function createPremiumPrice() {
  const stripe = new Stripe(process.env.STRIPE_SECRET_KEY, {
    apiVersion: '2024-04-10', // using a stable version
  });

  try {
    console.log("Creating Stripe Product...");
    const product = await stripe.products.create({
      name: 'Protein Gym Tracker Premium',
      description: 'Unlock custom goals, analytics, and ad-free tracking.',
    });

    console.log("Creating Stripe Price...");
    const price = await stripe.prices.create({
      product: product.id,
      unit_amount: 499, // $4.99
      currency: 'usd',
      recurring: {
        interval: 'month',
      },
    });

    console.log(`✅ Success! Created Price ID: ${price.id}`);

    // Append to .env file
    const envPath = path.resolve(__dirname, '../.env');
    let envFile = fs.readFileSync(envPath, 'utf8');
    
    // Check if it already exists
    if (!envFile.includes('STRIPE_PRICE_ID=')) {
        fs.appendFileSync(envPath, `\n# Generated by script\nSTRIPE_PRICE_ID="${price.id}"\n`);
        console.log("✅ Appended STRIPE_PRICE_ID to .env!");
    } else {
        // replace it
        envFile = envFile.replace(/STRIPE_PRICE_ID=".+"/, `STRIPE_PRICE_ID="${price.id}"`);
        fs.writeFileSync(envPath, envFile);
        console.log("✅ Updated existing STRIPE_PRICE_ID in .env!");
    }

  } catch (error) {
    console.error("Failed to create Stripe Price:", error.message);
  }
}

createPremiumPrice();
